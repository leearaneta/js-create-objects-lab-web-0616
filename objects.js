'use strict';

function Dog(name, breed, age) {
  this.name = name
  this.breed = breed
  this.age = age
}

function Cat(name, breed, age) {
  this.name = name
  this.breed = breed
  this.age = age
}

function Ferret(name, breed, age) {
  this.name = name
  this.age = breed
  this.foodRequirements = age
}

function Bird(name, breed) {
  this.name = name
  this.breed = breed
}

function Fish(name, breed, age) {
  this.name = name
  this.species = breed
  this.waterConditions = age
}
